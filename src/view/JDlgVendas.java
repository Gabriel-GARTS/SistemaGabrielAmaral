    public GaaVendas viewBean() {
        GaaVendas vendas = new GaaVendas();
        vendas.setGaaIdVendas(Util.strToInt(jTxtCodigo.getText()));
        vendas.setGaaClientes((GaaClientes) jCboClientes.getSelectedItem() );
        vendas.setGaaVendedor((GaaVendedor) jCboVendedor.getSelectedItem());
        vendas.setGaaDataPagamento(Util.strToDate(jFmtDataPagamento.getText()));
        vendas.setGaaDataVenda(Util.strToDate(jFmtDataVenda.getText()));
        vendas.setGaaTotal(Util.strToDouble(jTxtTotal.getText()));
        vendas.setGaaMetodoPagamento(jTxtMetodopag.getText());
        vendas.setGaaStatus (jTxtStatus.getText());
        return vendas;
    }
    
    public void beanView(GaaVendas vendas) {
        jTxtCodigo.setText(Util.intToStr(vendas.getGaaIdVendas()));
        jCboClientes.setSelectedItem(vendas.getGaaClientes());
        jCboVendedor.setSelectedItem(vendas.getGaaVendedor());
        jFmtDataPagamento.setText(Util.dateToStr(vendas.getGaaDataPagamento()));
        jFmtDataVenda.setText(Util.dateToStr(vendas.getGaaDataVenda()));
        jTxtTotal.setText(Util.doubleToStr(vendas.getGaaTotal()));
        jTxtStatus.setText(vendas.getGaaStatus());
        jTxtMetodopag.setText(vendas.getGaaMetodoPagamento());
}

